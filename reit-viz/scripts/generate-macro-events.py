# ============================================================
# NFP (Nonfarm Payrolls) Release Dates — first Friday of each month
# Source: BLS Employment Situation schedule
# ============================================================
def get_first_friday(year, month):
    """Return the first Friday of the given month."""
    d = date(year, month, 1)
    # 0=Mon ... 4=Fri
    days_until_friday = (4 - d.weekday()) % 7
    return d + timedelta(days=days_until_friday)

NFP_DATES = []
for y in range(2010, 2027):
    for m in range(1, 13):
        if y == 2026 and m > 4:
            break
        ff = get_first_friday(y, m)
        NFP_DATES.append(ff.isoformat())

